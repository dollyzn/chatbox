export const App = () => {
  const [user] = useAuthState(auth);

  if (!user) {
    {
      <BackdropLoading />;
    }
    return <Login />;
  }
  if (user) {
    return (
      <>
        {<BackdropLoading />}
        <Box className="bodyBox">
          <ChatBox />
        </Box>
      </>
    );
  }

  return <Box>Olá mundo</Box>;
};

